package src;

public class operators {
    public static void main(String[] args) throws Exception {
        int a =20;
        int b =4;
        System.out.println(a+b);
        System.out.println(a-b);

        System.out.println(a/b);
        System.out.println(a*b);

        System.out.println(a%b);

        System.out.println(10<<3);
        System.out.println(20>>2);

        System.out.println(~a);

    }
}
